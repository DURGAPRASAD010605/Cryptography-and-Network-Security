class ASCIIValues{
    public static void main(String [] args){
        System.out.println("ASSII Values of a to z :");
        for(char ch='a';ch<='z';ch++){
        System.out.println(ch + "=" +(int)ch);
        }
        System.out.println("ASSII Values of A to Z :");
        for(char ch='A';ch<='Z';ch++){
        System.out.println(ch + "=" +(int)ch);
        }

    }
}