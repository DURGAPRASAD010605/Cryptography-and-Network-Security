public class DESLogic {
    static int[] IP={
        58,50,42,34,26,18,10,2,
        60,52,44,36,28,20,12,4,
        62,54,46,38,30,22,14,6,
        64,56,48,40,32,24,16,8,
        57,49,41,33,25,17,9,1,
        59,51,43,35,27,19,11,3,
        61,53,45,37,29,21,13,5,
        63,55,47,39,31,23,15,7
    };

    static int[] IP_INV={
        40,8,48,16,56,24,64,32,
        39,7,47,15,55,23,63,31,
        38,6,46,14,54,22,62,30,
        37,5,45,13,53,21,61,29,
        36,4,44,12,52,20,60,28,
        35,3,43,11,51,19,59,27,
        34,2,42,10,50,18,58,26,
        33,1,41,9,49,17,57,25
    };

    static String permute(String input,int[] table){
        StringBuilder output=new StringBuilder();
        for(int i:table) {
            output.append(input.charAt(i-1));
        }
        return output.toString();
    }

    static String xor(String a,String b){
        StringBuilder result=new StringBuilder();
        for(int i=0;i<a.length();i++)
        {  
            result.append(a.charAt(i)^b.charAt(i));
        }
        return result.toString();
    }

    static String roundFunction(String right,String key){
        
        return xor(right,key.substring(0,32));
    }

    public static void main(String[] args){
        String plaintext="0000000100100011010001010110011110001001101010111100110111101111";
        String key="00101010010000100100100010100010101010101011010100";
        String ip=permute(plaintext,IP);
        String left=ip.substring(0,32);
        String right=ip.substring(32);
        
        String newRight=xor(left,roundFunction(right,key));
        left=right;
        right=newRight;
        String combined=right+left;
        String cipher=permute(combined,IP_INV);
        
        System.out.println("cipher Text: "+cipher);
    }
}
